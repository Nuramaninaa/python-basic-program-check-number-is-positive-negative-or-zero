#python program to check if a Number is Positive, Negative or Zero

#input from user
n = float(input("Enter the number : "))

#check if n greater than 0
if n > 0:
   print ("n is number positive")

#check if n less than 0
elif n < 0:
   print ("n is number negative")

#check if n is 0
else:
   print ("n is number zero")
